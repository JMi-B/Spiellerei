CREATE TABLE Patients (
    patient_id INT PRIMARY KEY,
    first_name VARCHAR(50),
    last_name VARCHAR(50),
    date_of_birth DATE,
    phone VARCHAR(20),
    email VARCHAR(100),
    address VARCHAR(255)
);

CREATE TABLE Producers (
    producer_id INT PRIMARY KEY AUTO_INCREMENT,
    producer_name VARCHAR(100) UNIQUE NOT NULL
);

CREATE TABLE Medications (
    drug_id INT PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    producer_id INT NOT NULL,
    contents_ml INT NOT NULL,
    price DECIMAL(7,2) NOT NULL,
    FOREIGN KEY (producer_id) REFERENCES Producers(producer_id)
);

CREATE TABLE Patient_Medications (
    patient_id INT,
    drug_id INT,
    dosage_ml DECIMAL(5,2),
    PRIMARY KEY (patient_id, drug_id),
    FOREIGN KEY (patient_id) REFERENCES Patients(patient_id),
    FOREIGN KEY (drug_id) REFERENCES Medications(drug_id)
);

CREATE TABLE Patient_Conditions (
    condition_id INT PRIMARY KEY AUTO_INCREMENT,
    condition_name VARCHAR(100) UNIQUE NOT NULL
);

CREATE TABLE Patient_Diagnoses (
    patient_id INT,
    condition_id INT,
    diagnosis_date DATE,
    PRIMARY KEY (patient_id, condition_id),
    FOREIGN KEY (patient_id) REFERENCES Patients(patient_id),
    FOREIGN KEY (condition_id) REFERENCES Patient_Conditions(condition_id)
);

